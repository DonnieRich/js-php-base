// Conta le proprietà di un oggetto
const user = { name: "Alice", age: 25, role: "admin" }; // 3

// Trova la proprietà con il valore più alto
const scores = { math: 90, english: 85, science: 95 }; // "science"

// Converti un oggetto in un array di coppie chiave-valore (senza uso di Object.entries)
const car = { brand: "Tesla", model: "X", year: 2024 }; // [["brand", "Tesla"], ["model", "X"], ["year", 2024]]

// Unisci due oggetti in uno nuovo (senza uso di Object.assign o spread operator)
const obj1 = { a: 1, b: 2 };
const obj2 = { b: 3, c: 4 }; // {a:1, b:3, c:4}